/**
 * @fileoverview 趣味互动模块的 System Prompts
 *
 * ## 服务职责
 * 本模块是 "趣味互动"（Play）业务场景的 **prompt 工程层**，负责定义发送给
 * 大语言模型（DeepSeek）的 system prompt 模板。它不包含任何运行时逻辑、
 * API 调用或状态管理——仅作为 prompt 资产的集中管理点。
 *
 * ## 架构定位
 * ```
 * 客户端请求 → playController（路由/校验）
 *                   → playService（组装上下文、调用 LLM、解析响应）
 *                        → playPrompts.ts（本模块：提供 system prompt 模板）
 * ```
 * 上层 service 从本模块导入对应的 prompt 常量，与用户输入拼接后通过
 * DeepSeek API 完成推理。将 prompt 从业务逻辑中抽离的好处：
 * - prompt 迭代（调优措辞、调整输出格式）无需改动 service 代码
 * - 便于 A/B 测试不同 prompt 版本
 * - 集中管理所有"儿童不宜内容"的安全护栏声明
 *
 * ## 安全约束
 * **所有 prompt 均包含儿童安全过滤指令**，要求模型严禁输出恐怖、暴力、
 * 死亡、色情等不适宜未成年人的内容。这是防御性的第一道护栏；第二道护栏
 * 在 service 层对模型原始输出做关键字/正则后过滤。
 *
 * ## 目标模型
 * DeepSeek（deepseek-chat / deepseek-reasoner）。prompt 使用中文编写，
 * 因为该模型在中文创意写作任务上表现更稳定。
 *
 * @module playPrompts
 * @see {@link ../controllers/playController.ts} 路由与参数校验
 * @see {@link ./playService.ts} 调用本模块 prompt 的 service 层
 */

// ============================================================================
// STORY_PROMPT — 儿童专属故事生成
// ============================================================================

/**
 * 故事生成的 system prompt。
 *
 * 用于 `fun/story` 接口：根据小朋友的年龄、兴趣标签（如"恐龙""公主"）
 * 创作一个 200-500 字的个性化故事。上层 service 会将用户提供的参数注入
 * 到 user message 中，与这段 system prompt 组合发送。
 *
 * @constant {string} STORY_PROMPT
 *
 * @design
 * ## 长度控制（200-500 字）
 * 限制字数有两个目的：
 * 1. 适配 3-8 岁儿童的注意力跨度，过长会失去兴趣
 * 2. 控制 token 消耗，避免单次请求成本过高
 *
 * ## 正面价值观
 * "善良、勇敢、分享"是经过产品侧筛选的三大核心品质，覆盖家长最关心的
 * 教育维度。如果后续需要扩展，应通过 A/B 测试验证新词汇的接受度。
 *
 * ## 安全护栏
 * 第 6 条是硬性过滤指令。实际效果依赖模型遵循能力——service 层会额外
 * 做输出侧校验作为兜底。
 */
export const STORY_PROMPT = `你是一位温暖、有创意的儿童故事作家。请根据以下信息，为小朋友创作一个专属故事。

要求：
1. 故事长度适中（200-500字），适合指定年龄段
2. 语言生动有趣，适当使用拟声词和重复句式
3. 故事内容积极向上，传达善良、勇敢、分享等正面价值观
4. 如果小朋友有特别兴趣（如恐龙、公主），融入故事主题
5. 故事结尾可以有一个温暖的小道理
6. 严禁包含恐怖、暴力、黑暗、死亡等不适宜儿童的内容
7. 可以使用适当的 emoji 增强趣味性`;

// ============================================================================
// RIDDLE_PROMPT — 儿童谜语生成（结构化 JSON 输出）
// ============================================================================

/**
 * 谜语生成的 system prompt，要求模型输出 **严格 JSON 格式**。
 *
 * 用于 `fun/riddle` 接口：根据小朋友年龄生成难度匹配的谜语，
 * 附带分类标签和三个阶梯式提示。上层 service 负责 `JSON.parse` 并校验
 * 字段完整性，解析失败时触发降级逻辑（返回兜底谜语或重试）。
 *
 * @constant {string} RIDDLE_PROMPT
 *
 * @design
 * ## 难度分级策略
 * - **3-5 岁（easy）**：谜面围绕日常物品（水果、动物、身体部位），
 *   特征描述直接，答案在儿童认知范围内。
 * - **5-8 岁（medium/hard）**：引入简单逻辑推理（因果关系、排除法），
 *   谜面可包含双关或轻度误导，但仍需在儿童生活经验圈内。
 *
 * ## 三级提示体系
 * `hints[0]` → `hints[1]` → `hints[2]` 构成递增难度曲线：
 * - 提示 1：几乎直接透露答案类别（如"它是一种水果哦"）
 * - 提示 2：缩小范围但保留思考空间
 * - 提示 3：接近答案但不直接说出
 * 这样设计让家长可以按需释放提示，延长亲子互动时间。
 *
 * ## 输出契约（Service 层依赖）
 * JSON schema:
 * ```
 * {
 *   riddle: string;       // 谜面文字
 *   category: string;     // 动物｜生活｜自然｜食物
 *   difficulty: string;   // "easy" | "medium" | "hard"
 *   answer: string;       // 谜底
 *   hints: string[];      // 长度 = 3，难度递增
 * }
 * ```
 * 如果模型未输出合法 JSON，service 层应：
 * 1. 尝试正则提取 JSON 子串
 * 2. 失败则重试 1 次
 * 3. 仍失败则返回预设兜底谜语
 *
 * ## 安全护栏
 * "严禁不适宜内容"与 STORY_PROMPT 的安全指令等效，但因谜语输出是结构化的，
 * service 层可额外对 `answer` 和 `riddle` 字段做关键词黑名单过滤。
 */
export const RIDDLE_PROMPT = `你是一位幽默风趣的儿童谜语大师。请根据小朋友的年龄，设计一个有趣的谜语。

要求：
1. 谜语难度与年龄匹配（3-5岁简单生活类，5-8岁稍复杂逻辑类）
2. 谜底必须是小朋友认知范围内的物品、动物、自然现象等
3. 谜面押韵、有趣、易懂
4. 提供3个递增难度的提示
5. 严禁不适宜内容

请严格按以下JSON格式输出（仅输出JSON）：
{
  "riddle": "谜面文字",
  "category": "分类（如：动物/生活/自然/食物）",
  "difficulty": "easy|medium|hard",
  "answer": "谜底",
  "hints": ["提示1（最容易）", "提示2", "提示3"]
}`;

